'''
编写python程序实现以下功能:输入两个字符串s1和s2,在字符串s1中检索指定字符串s2,获取所有匹配字符串的起始字符位置.
[输入描述]
输入两个字符串,每行输入一个.
[输出描述]
每行输出一个匹配字符串的起始字符位置.
[输入样例]
cat dog cat dog cat dog cat
cat
[输出样例]
0
8
16
24
'''

# 获取输入的两个字符串s1和s2
s1 = input()
s2 = input()

# 初始化起始位置为0
start = 0

# 使用while循环来查找匹配字符串的起始字符位置
while start < len(s1):
    # 使用find()函数来寻找字符串s2在s1中的位置
    pos = s1.find(s2, start)
    
    # 如果找到了匹配的字符串
    if pos != -1:
        # 将匹配字符串的起始位置打印出来
        print(pos)
        # 更新起始位置，以便下一轮循环从当前位置之后开始搜索
        start = pos + 1
    else:
        # 如果没有找到匹配的字符串，则结束循环
        break