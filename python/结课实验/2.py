'''
中国古代数学家张丘建在<算经>中提出了著名的"百钱百鸡问题":鸡翁一,值钱五;鸡母一,值钱三;鸡雏三,值钱一;百钱买百鸡,翁,母,雏各几何?把问题转化为用n元钱买n只鸡,n为任意正整数,求可实施方案.
[输入描述]
n值,表示购买三只鸡总数目.
[输出描述]
可实施方案,每行代表一个.
[输入样例]
100
[输出样例]
公鸡:0   母鸡:25  小鸡:75
公鸡:4   母鸡:18  小鸡:78
公鸡:8   母鸡:11  小鸡:81
公鸡:12  母鸡:4   小鸡:84

'''
# 这段代码用于解决"百钱百鸡问题"
# 根据输入的n值，表示购买三只鸡总数目，计算可实施方案并输出

n = int(input(""))

# 可行解决方案的计算和输出
for i in range(0, n//5 + 1):
    for j in range(0, n//3 + 1):
        k = n - i - j
        if k % 3 == 0 and i*5 + j*3 + k//3 == n:
            print(f"公鸡:{i} 母鸡:{j} 小鸡:{k}")