;假设x、y、r、w均为16位带符号整数，分别保存于字单元X、Y、R和W字存储
;单元。编写程序计算(w + x*y + 230)/(r-80)/2。 表达式计算结果存于
;字单元Z中(忽略余数)。假设(r-80)不会超出16位带符号数表示范围，
;整个表达式结果不会超出16位符号数表示范围。处理完毕显示'OK!'信息。
;要求源程序中包含完善的注释信息。做题时不要损坏题中原有的注释线。
