DATAS SEGMENT
	X	DW 22
	Y 	DW 2233
	R	DW 180
	W	DW 8765
;-------------------------所需变量定义于两横线之间-------------------
	OK	DB 'OK!',0dh,0ah,'$'
	Z	DW ?
;------------------------------------------------------------------------
DATAS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,ES:DATAS
START:
    MOV AX,DATAS
    MOV DS,AX
    MOV ES,AX
;--------------------------程序代码写于两横线之间---------------------
	MOV AX, X
	IMUL Y
	ADD AX, W
	ADC DX, 0
	ADD AX, 230
	ADC DX, 0
	MOV BX, R
	SUB BX, 80
	IDIV BX
	SAR AX,1
	MOV Z,AX
	MOV AH,9
	MOV DX, OFFSET Ok
	INT 21H	
;--------------------------------------------------------------------------
;其它处理（代码略） 
    MOV AX,4C00H
    INT 21H
CODES ENDS
    END START