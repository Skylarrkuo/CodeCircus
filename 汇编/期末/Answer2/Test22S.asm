;单元。编写程序计算(w * x - 50) * 16 /(y + 6), 余数保存于字单元R中，
;商保存于字单元Z中。(假设(w * x - 50) * 16 不会超过32位带符号数表
;示范围，y+6不会超过16位带符号数表示范围)。要求乘以16必须使用移位
;运算实现，处理完毕显示'OK!'信息。
;要求源程序中包含完善的注释信息。做题时不要损坏题中原有的注释线。
