;(3)计算出(w * x - 50)得3分,计算出(w * x - 50) * 4得3分,
;计算出(w * x - 50) * 4 /(y + 6)得3分，正确地保存结果得3分;
;(4)指令无语法错误，能编译成EXE文件4分。
