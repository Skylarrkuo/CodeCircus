DATAS SEGMENT
	X	DW 22
	Y 	DW 2233
	R	DW ?
	W	DW 8765
;-------------------------所需变量定义于两横线之间-------------------
	OK	DB 'OK!',0dh,0ah,'$'
	Z	DW ?
;------------------------------------------------------------------------
DATAS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,ES:DATAS
START:
    MOV AX,DATAS
    MOV DS,AX
    MOV ES,AX
;--------------------------程序代码写于两横线之间---------------------
	MOV AX, W
	IMUL X
	SUB AX, 50
	SBB DX, 0
	MOV CL, 4  ;|
	SAL DX, CL ;|
	MOV BL, AH ;>把（DX，AX）算术左移4位(即乘以16)
	SHL AX, CL ;|
	SHR BL, CL ;|
	OR DL,BL   ;|
	MOV BX, Y
	ADD BX, 6
	IDIV BX
	MOV R,DX
	MOV Z,AX
;--------------------------------------------------------------------------
;其它处理（代码略） 
	MOV AH,9
	MOV DX, OFFSET Ok
	INT 21H
    MOV AX,4C00H
    INT 21H
CODES ENDS
    END START