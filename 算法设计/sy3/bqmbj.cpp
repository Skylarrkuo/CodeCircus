//百钱买百鸡
#include <stdio.h>

int main() {
   int x, y, z;
    for (x = 0; x <= 20; x += 3) {  // 公鸡每次增加 3 只 
        for (y = 0; y <= 33; y++) {  // 母鸡最多买 33 只
            z = 100 - x - y;  // 计算小鸡的数量
            if (z % 3 == 0 && 5 * x + 3 * y + z / 3 == 100) {  // 检查小鸡数量是否为整数,以及总价是否为 100
                printf("公鸡:%d只,母鸡:%d只,小鸡:%d只\n", x, y, z);
            }
        }
    }
    return 0;
}
